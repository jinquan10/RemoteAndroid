package com.miw.remoid;

public class BrowserRequest {
	private int op;
	private int x;
	private int y;

	public int getOp() {
		return op;
	}
	public void setOp(int op) {
		this.op = op;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
